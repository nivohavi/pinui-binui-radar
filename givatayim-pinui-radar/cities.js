function whatBudgetBuys(budget) {
  const results = [];
  for (const [slug, city] of Object.entries(CITIES)) {
    const affordable = city.zones.filter(z => { const min = parsePriceMin(z.prices.rows[0] ? z.prices.rows[0][1] : '0'); return min > 0 && min <= budget; })
      .map(z => ({ zone: z.name, zoneId: z.id, status: z.status, priceLabel: z.prices.rows[0][1], premium: (z.prices.rows.find(r => r[0].includes('פרמיי')) || ['-','-'])[1], valueScore: computeValueScore(z) }))
      .sort((a, b) => (b.valueScore || 0) - (a.valueScore || 0));
    results.push({ city: city.name, slug, zones: affordable });
  }
  return results;
}

function checkForStatusChanges() {
  const key = 'pinui_zone_statuses', current = {};
  for (const [slug, city] of Object.entries(CITIES)) city.zones.forEach(z => current[slug + '/' + z.id] = z.status);
  const prev = JSON.parse(localStorage.getItem(key) || '{}'), changes = [];
  for (const [id, status] of Object.entries(current)) {
    if (prev[id] && prev[id] !== status) {
      const [slug, zid] = id.split('/'), city = CITIES[slug], zone = city ? city.zones.find(zn => zn.id === zid) : null;
      changes.push({ cityName: city ? city.name : slug, zoneName: zone ? zone.name : zid, from: prev[id], to: status });
    }
  }
  localStorage.setItem(key, JSON.stringify(current));
  return changes;
}

