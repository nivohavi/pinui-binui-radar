// Helpers
function getCurrentCity() {
  const url = new URL(window.location.href);
  const slug = url.searchParams.get('city') || 'givatayim';
  return CITIES[slug] || CITIES.givatayim;
}

function setCurrentCity(slug) {
  const url = new URL(window.location.href);
  url.searchParams.set('city', slug);
  window.location.href = url.toString();
}

function buildCitySelector(currentSlug) {
  const cities = [['givatayim','גבעתיים'],['ramat-gan','רמת גן'],['tel-aviv','תל אביב']];
  const html = cities.map(([slug,name]) => {
    const active = slug===currentSlug ? ' active' : '';
    return `<button class="city-btn${active}" onclick="setCurrentCity('${slug}')">${name}</button>`;
  }).join('');
  return `<div class="city-selector">${html}</div>`;
}

// Append "?city=xxx" to all internal .html links so navigation preserves city
function preserveCityInLinks() {
  const currentSlug = getCurrentCity().slug;
  document.querySelectorAll('a[href$=".html"], a[href*=".html#"]').forEach(a => {
    const href = a.getAttribute('href');
    if (href.startsWith('http')) return;
    const [path, hash] = href.split('#');
    const newUrl = path + '?city=' + currentSlug + (hash ? '#' + hash : '');
    a.setAttribute('href', newUrl);
  });
}

