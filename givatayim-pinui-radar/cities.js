function parsePriceMin(str) { const m = str.match(/([\d.]+)M/); return m ? parseFloat(m[1]) * 1e6 : 0; }
function parsePremiumMid(rows) { const r = rows.find(r => r[0].includes('פרמיי')); if (!r) return 0; const m = r[1].match(/(\d+)[–\-](\d+)/); return m ? (parseInt(m[1]) + parseInt(m[2])) / 2 : 0; }
function parseTimelineMid(facts) { const r = facts.find(f => ['אופק', 'אכלוס', 'זמן', 'שנים'].some(k => f[0].includes(k) || f[1].includes(k))); if (!r) return 0; const m = r[1].match(/(\d+)[–\-](\d+)/); if (m) return (parseInt(m[1]) + parseInt(m[2])) / 2; const s = r[1].match(/(\d+)/); return s ? parseInt(s[1]) : 0; }
function parsePpsqmRange(rows) { const r = rows.find(r => r[0].includes('מחיר למ')); if (!r) return null; const m = r[1].match(/([\d.]+)\s*[–\-]\s*([\d.]+)\s*K/i) || r[1].match(/([\d.]+)K\s*[–\-]\s*([\d.]+)K/i); if (m) return ((parseFloat(m[1]) + parseFloat(m[2])) / 2) * 1000; const s = r[1].match(/([\d.]+)K/i); return s ? parseFloat(s[1]) * 1000 : null; }

function computeValueScore(zone) {
  const premium = parsePremiumMid(zone.prices.rows), years = parseTimelineMid(zone.facts);
  const riskMul = { yes: 1.0, maybe: 0.65, no: 0.25 }[zone.status] || 0;
  if (zone.status === 'yes' && !premium) return years ? parseFloat((3 / Math.max(years, 1)).toFixed(1)) : 0;
  return (!premium || !years) ? null : parseFloat(((premium / years) * riskMul).toFixed(1));
}

