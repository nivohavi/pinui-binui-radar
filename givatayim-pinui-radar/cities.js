// Build deep-search URLs for Yad2, Madlan, Facebook, Nadlan.gov.il, Homeless, Komo.
// Usage: buildSearchLinks(city, { hood, rooms, maxPrice })
//   city       - city object (from getCurrentCity or CITIES[slug])
//   hood       - optional neighborhood/zone string (Hebrew, e.g. "בורוכוב")
//   rooms      - optional string like "3-4" (Yad2 format)
//   maxPrice   - optional number (NIS) — max price cap
function buildSearchLinks(city, opts) {
  opts = opts || {};
  const codes = city.searchCodes;
  const hood = (opts.hood || '').trim();
  const rooms = opts.rooms || '';
  const maxPrice = opts.maxPrice ? String(opts.maxPrice) : '';

  // ── Yad2 ─ apartments only + neighborhood ID (if available) — VERIFIED working
  const yad2 = new URL('https://www.yad2.co.il/realestate/forsale');
  if (codes.yad2TopAreaId) yad2.searchParams.set('topArea', codes.yad2TopAreaId);
  if (codes.yad2AreaId)    yad2.searchParams.set('area', codes.yad2AreaId);
  yad2.searchParams.set('city', codes.yad2CityId);
  const hoodId = hood && YAD2_HOOD_IDS[city.slug] && YAD2_HOOD_IDS[city.slug][hood];
  if (hoodId) yad2.searchParams.set('neighborhood', hoodId);
  yad2.searchParams.set('property', '1');
  if (rooms) yad2.searchParams.set('rooms', rooms);
  if (maxPrice) yad2.searchParams.set('price', '-1-' + maxPrice);

  // ── Madlan ─ city-level only (hood-level slugs are unreliable — Madlan has custom slugs per hood).
  // The Madlan city page supports filtering by hood within the UI.
  const madlan = 'https://www.madlan.co.il/for-sale/' + encodeURIComponent(codes.madlanSlug);

  // ── Facebook ─ posts search (shows results in groups+pages the user is a member of, when logged in).
  const fbQ = [hood, codes.fbQueryBase].filter(Boolean).join(' ');
  const facebook = 'https://www.facebook.com/search/posts/?q=' + encodeURIComponent(fbQ);

  // ── Nadlan.gov.il — official closed-deals price data
  const nadlanQ = [hood, codes.nadlanCity].filter(Boolean).join(' ');
  const nadlan = 'https://www.nadlan.gov.il/?search=' + encodeURIComponent(nadlanQ);

  // ── Homeless.co.il — URL format is /sale/city=CITY (path segment, not query).
  // No reliable neighborhood filter via URL — city-level only.
  const homeless = 'https://www.homeless.co.il/sale/city=' + encodeURIComponent(codes.homelessCity);

  // ── Komo (ynet classifieds) — URL format /code/nadlan/apartments-for-sale.asp?cityName=CITY.
  // Replaces WinWin (which was decommissioned).
  const komo = 'https://www.komo.co.il/code/nadlan/apartments-for-sale.asp?cityName=' + encodeURIComponent(codes.komoCity);

  return {
    yad2: yad2.toString(),
    madlan,
    facebook,
    nadlan,
    homeless,
    komo,
    _deep: !!hoodId
  };
}

// Build multiple Facebook links at once: posts search + groups discovery + marketplace.
// Returns array of [label, url] tuples. All URLs are public-ish — no fake group IDs.
function buildFacebookLinks(city, opts) {
  opts = opts || {};
  const codes = city.searchCodes;
  const hood = (opts.hood || '').trim();
  const q = [hood, codes.fbQueryBase].filter(Boolean).join(' ');
  const qEnc = encodeURIComponent(q);
  const out = [];

  // Posts search — surfaces posts from groups the user is ALREADY in + public pages
  out.push(['🔍 חיפוש פוסטים', 'https://www.facebook.com/search/posts/?q=' + qEnc]);
  // Groups discovery — find more groups on the topic
  out.push(['🔎 חיפוש קבוצות', 'https://www.facebook.com/search/groups/?q=' + qEnc]);
  // Marketplace — sale listings
  const mpQ = [hood, codes.komoCity || codes.nadlanCity].filter(Boolean).join(' ');
  out.push(['🛒 Marketplace', 'https://www.facebook.com/marketplace/search/?query=' + encodeURIComponent(mpQ) + '&category=propertyforsale']);

  return out;
}

